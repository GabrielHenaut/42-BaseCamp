int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && i < n)
	{
		if (s1[i] > s2[i])
		{
			return (1);
		}
		else if (s1[i] < s2[i])
		{
			return (-1);
		}
		i++;
	}
	return (0);
}


#include <stdio.h>

int	main(void)
{
	char	*a = "abcde";
	char	*b = "abcdfas";
	int		size = 5;
	
	printf("%i\n", ft_strncmp(a, b, size));
}